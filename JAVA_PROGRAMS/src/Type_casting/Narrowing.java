package Type_casting;

public class Narrowing {
public static void main(String[] args) {
	 System.out.println("Narrowing explicitely");
	    int a=(int) 20.0d;
	    System.out.println(a);
	    
	    float b=(float) 20.0d;
	    System.out.println(b);
	    
	    int c=(int) 30.0d;
	    System.out.println(c);
}
}
